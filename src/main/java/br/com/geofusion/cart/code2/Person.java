package br.com.geofusion.cart.code2;

public class Person {

  private String name;

  private String city;

  public Person(String name, String city) {
    super();
    this.name = name;
    this.city = city;
  }

  public String getCity() {
    return city;
  }

  public String getName() {
    return name;
  }

}
